package com.example.financlick.json;

public class CambioObj {
    private String compraUSD;
    private String vendaUSD;
    private String compraEUR;
    private String vendaEUR;


    public String getCompraUSD() {
        return compraUSD;
    }

    public void setCompraUSD(String compraUSD) {
        this.compraUSD = compraUSD;
    }

    public String getVendaUSD() { return vendaUSD; }

    public void setVendaUSD(String vendaUSD) {
        this.vendaUSD = vendaUSD;
    }

    public String getCompraEUR() {
        return compraEUR;
    }

    public void setCompraEUR(String compraEUR) {
        this.compraEUR = compraEUR;
    }

    public String getVendaEUR() { return vendaEUR; }

    public void setVendaEUR(String vendaEUR) {
        this.vendaEUR = vendaEUR;
    }

}

